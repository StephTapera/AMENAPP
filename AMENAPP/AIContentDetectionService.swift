/// Service to detect AI-generated content in posts
class AIContentDetectionService {
    static let shared = AIContentDetectionService()
    
    private init() {}
    
    // MARK: - AI Detection Patterns
    
    /// Common AI-generated content patterns and phrases
    private let aiPatterns = [
