        "genuine peace", "authentic joy", "keep choosing",
        "keep remembering", "never forget", "always remember"
    ]
    
    /// AI source identifiers and artifacts (ChatGPT, Claude, etc.)
    private let aiSourceArtifacts = [
        // ChatGPT artifacts
        "chatgpt", "chat gpt", "gpt-4", "gpt-3", "openai",
        "generated by chatgpt", "from chatgpt",
        
        // Claude artifacts  
        "claude", "anthropic", "generated by claude", "from claude",
        
