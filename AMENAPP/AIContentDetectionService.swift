        "as an ai", "as a language model", "i'm an ai", "i am an ai",
        "i don't have personal", "i cannot provide personal",
        "i'm unable to", "i cannot recommend", "i apologize, but",
        "as an artificial", "i'm a large language model",
        
        // Generic AI phrases
        "certainly!", "absolutely!", "here's a breakdown",
        "in summary:", "to summarize:", "key takeaways:",
        "it's important to note", "it's worth noting",
        "it's crucial to", "it's essential to",
        "here are some", "here's what you need to know",
        "let me explain", "let's break this down",
        "to put it simply", "in other words",
        "the key point is", "the main takeaway",
        
        // Overly enthusiastic AI language
        "i'd be happy to", "i'd be glad to",
        "great question!", "excellent question!",
        "happy to help", "glad you asked",
        
        // Formal transitions
        "furthermore", "moreover", "additionally",
        "consequently", "therefore", "thus",
        "in conclusion", "to conclude",
        "that being said", "with that in mind"
    ]
    
